
package javabasic;


public class IncDecrement {
     public static void main(String[] args)
     {
         int x=25,y;
         
         y=x++;
         System.out.println("y :"+y);
          
          y=x;
          System.out.println("y :"+y);
          
          y=++x;
          System.out.println("y :"+y);
          
          y=x;
          System.out.println("y :"+y);
          
          y=x--;
          System.out.println("y :"+y);
          
          y=x;
          System.out.println("y :"+y);
          
          y=--x;
          System.out.println("y :"+y);
          
          y=x;
          System.out.println("y :"+y);
     }
     
           
    
}
